public class App {
    public static void main(String[] args) {
        
        int number1 = 123;
        int number2 = 456;

        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 / number2);
        System.out.println(number1 * number2);
        System.out.println(number1 % number2);

        number1++;
        System.out.println(number1);

        int num = 10;
        num += 10;
        System.out.println(num);
        num -= 10;
        System.out.println(num);

    }
}
